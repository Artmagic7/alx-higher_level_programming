#!/usr/bin/python3
def add(a, b):
    """My additn functn

    Args:
        a: 1st integer
        b: 2nd integer

    Returns:
        The return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My subtractn functn

    Args:
        a: 1st integer
        b: 2nd integer

    Returns:
        The return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My multiplicatn functn

    Args:
        a: 1st integer
        b: 2nd integer

    Returns:
        The return value. a * b
    """
    return (a * b)


def div(a, b):
    """My divisn function

    Args:
        a: 1st integer
        b: 2nd integer

    Returns:
        The return value. a / b
    """
    return int(a / b)
